def _build_lookup() -> list[tuple[str, str]]:
    """返回 (alias, canonical) 列表，按别名长度降序，避免短词抢匹配."""
    pairs: list[tuple[str, str]] = []
    for canonical, aliases in {**_REGION_ALIASES, **_CITY_ALIASES}.items():
        for alias in aliases:
            pairs.append((alias, canonical))
    pairs.sort(key=lambda x: len(x[0]), reverse=True)
    return pairs


_LOOKUP = _build_lookup()


def extract_regions(text: str) -> list[str]:
    """从文本中提取标准化区域列表."""
    found: list[str] = []
    remaining = text
    for alias, canonical in _LOOKUP:
        if alias in remaining:
            if canonical not in found:
                found.append(canonical)
            # 避免重复匹配同一位置时仍允许多区域
            remaining = remaining.replace(alias, " ", 1)
    return found


def strip_regions(text: str) -> str:
    """从文本中移除已识别区域词，便于关键词抽取."""
    result = text
    for alias, _ in _LOOKUP:
        if alias in result:
            result = result.replace(alias, " ")
    return result
